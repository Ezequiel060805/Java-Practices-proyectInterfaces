    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
           
        try {
            // obtener los valores de los componentes que escribió el usuario
            String nombreUsuario = txtNombreUsuario.getText();
            String email = txtEmail.getText();
            String celular = txtCelular.getText();
            String rol = cboRol.getSelectedItem().toString();
            // crear un usuario con los 4 valores mínimos solicitados
            user = new Usuario(nombreUsuario, celular, email, rol);
            /* si la etiqueta (label) del id del usuario está vacía significa que no
            se va a modificar un usuario, lo que se quiere hacer es registrar uno nuevo
            */
            if (lblIdUsuario.getText().isEmpty()){
                // crear un nuevo usuario
                /* como ya se mencionó en la clase Usuario, el método al ejecutar un Insert Into
                retorna un valor entero. Lo que se está haciendo es preguntar si es diferente a cero, 
                esto indica que si se inserto. Si diera 0 es que la operación no fue exitosa.
                */
                
                if (user.crear() != 0)
                    JOptionPane.showMessageDialog(null, "Usuario registrado");
                else
                    JOptionPane.showMessageDialog(null, "Usuario no fue posible registrar");
            } else{
                /* al dar falso la pregunta del label, es porque se quiere modificar un usuario,
                entonces, se toma el valor del label para conocer el id del usuario a modificar
                */
                int idUsuario = Integer.parseInt(lblIdUsuario.getText());
                
                if (user.modificar(idUsuario) != 0)
                    JOptionPane.showMessageDialog(null, "Usuario modificado");
                else
                    JOptionPane.showMessageDialog(null, "Usuario no fue posible modificar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Favor de verificar estén correctos y completos los datos del Usuario. Error: " + ex);
        }
        // método para vaciar los componentes
        limpiarDatos();        // TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarActionPerformed

