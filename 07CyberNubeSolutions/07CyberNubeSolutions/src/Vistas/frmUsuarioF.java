    private void btnModificar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificar2ActionPerformed
    try{
            /* al seleccionar un registro en el JTable y el usuario presiona el botón modificar
            se van a asignar a los elementos del primer panel para ser modificados y después para 
            guardar los cambios se debe dar clic en botón Registrar
            */
            DefaultTableModel modelo = (DefaultTableModel) tblUsuario.getModel();
            /* para obtener un valor de una celda del JTable se utiliza este método, el primer 
            argumento es el número de la fila (row) por ello se indica que será la fila seleccionada
            y para el segundo argumento se indica la columna, estás inician desde 0
            */
            lblIdUsuario.setText(modelo.getValueAt(tblUsuario.getSelectedRow(), 0).toString());
            txtNombreUsuario.setText(modelo.getValueAt(tblUsuario.getSelectedRow(), 1).toString());
            txtCelular.setText(modelo.getValueAt(tblUsuario.getSelectedRow(), 2).toString());
            txtEmail.setText(modelo.getValueAt(tblUsuario.getSelectedRow(), 3).toString());
            cboRol.setSelectedItem(modelo.getValueAt(tblUsuario.getSelectedRow(), 4).toString());
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Favor de seleccionar el usuario en la tabla. Error: " + e);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnModificar2ActionPerformed

